<script setup>
import { ref, onMounted } from 'vue';

const users = ref([]);
const error = ref('');

const fetchUsers = async () => {
  try {
    const response = await fetch('http://localhost:3000/users', {
      credentials: 'include',
    });

    if (!response.ok) {
      error.value = '‚ùå Du har inte beh√∂righet (endast admin)';
      return;
    }

    users.value = await response.json();
  } catch (err) {
    console.error('Error fetching users:', err);
    error.value = '‚ùå Kunde inte h√§mta anv√§ndare';
  }
};

const deleteUser = async (id) => {
  const confirmDelete = confirm(
    '√Ñr du s√§ker p√• att du vill ta bort denna anv√§ndare?'
  );
  if (!confirmDelete) return;

  try {
    const response = await fetch(`http://localhost:3000/users/${id}`, {
      method: 'DELETE',
      credentials: 'include',
    });

    if (response.ok) {
      users.value = users.value.filter((user) => user._id !== id);
    } else {
      error.value = '‚ùå Kunde inte ta bort anv√§ndaren';
    }
  } catch (err) {
    console.error('Delete error:', err);
    error.value = '‚ùå Serverfel vid borttagning';
  }
};

onMounted(() => {
  fetchUsers();
});
</script>

<template>
  <div class="adminpanel">
    <h2>Adminpanel - Alla anv√§ndare</h2>

    <div v-if="error" class="error">{{ error }}</div>

    <div v-if="users.length">
      <div v-for="user in users" :key="user._id">
        <div class="user">
          <p><strong>Anv√§ndarnamn:</strong> {{ user.username }}</p>
          <p><strong>L√∂senord (hashed):</strong> {{ user.password }}</p>
          <p><strong>Admin?:</strong> {{ user.is_admin ? 'Ja' : 'Nej' }}</p>
          <p>
            <strong>Skapad:</strong>
            {{ new Date(user.created_at).toLocaleString('sv-SE') }}
          </p>

          <button @click="deleteUser(user._id)">üóëÔ∏è Ta bort</button>
        </div>
      </div>
    </div>
  </div>
</template>

